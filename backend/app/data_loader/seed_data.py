# For regions without explicit municipalities — generate some
GENERIC_MUNIS = [
    ("Центральный г.о.", "городской_округ"),
    ("Районный МР", "муниципальный_район"),
]


def _gen_population_series(base_pop: int, years: range, trend: float) -> list[int]:
    """Generate population time series with trend and noise."""
    pops = []
    p = base_pop
    for _ in years:
        p = int(p * (1 + trend + random.gauss(0, 0.005)))
        pops.append(max(1000, p))
    return pops


async def seed_database():
    """Seed the database with demo data."""
    async with async_session() as db:
        existing = await db.execute(select(Region).limit(1))
        if existing.scalars().first():
            print("Database already seeded.")
            return

        print("Seeding database...")
        years = range(2010, 2023)

        # Create regions
        region_map = {}
        for code, name, fd in REGIONS_DATA:
            region = Region(code=code, name=name, federal_district=fd)
            db.add(region)
            region_map[code] = region

        await db.flush()

        # Create municipalities and data
        muni_count = 0
        for code, region in region_map.items():
            if code in MUNICIPALITIES_DATA:
                munis = MUNICIPALITIES_DATA[code]
            else:
                # Generate 2-3 generic municipalities
                base_pop = random.randint(30000, 500000)
                munis = [
                    (f"{region.name} - г.о.", "городской_округ", base_pop,
                     random.uniform(41, 70), random.uniform(28, 180)),
                    (f"{region.name} - МР", "муниципальный_район", int(base_pop * 0.3),
                     random.uniform(41, 70), random.uniform(28, 180)),
                ]

            for muni_data in munis:
                name, mtype, base_pop = muni_data[0], muni_data[1], muni_data[2]
                lat = muni_data[3] if len(muni_data) > 3 else None
                lon = muni_data[4] if len(muni_data) > 4 else None

                muni = Municipality(
                    oktmo_code=f"{code}{muni_count:04d}",
                    name=name,
                    municipality_type=mtype,
                    region_id=region.id,
                    latitude=lat,
                    longitude=lon,
                )
                db.add(muni)
                await db.flush()
                muni_count += 1

                # Determine trend
                growing_regions = {"77", "78", "50", "23", "16", "47", "92", "86", "89"}
                if code in growing_regions:
                    trend = random.uniform(0.005, 0.025)
                elif code in {"05", "06", "20"}:
                    trend = random.uniform(0.01, 0.03)
                else:
                    trend = random.uniform(-0.015, 0.005)

                pop_series = _gen_population_series(base_pop, years, trend)

                for i, year in enumerate(years):
                    pop = pop_series[i]
                    pr = PopulationRecord(
                        municipality_id=muni.id,
                        year=year,
                        population=pop,
                    )
                    db.add(pr)

                    # Demographics
                    avg_pop = pop
                    birth_rate = round(random.uniform(7.0, 18.0), 1)
                    death_rate = round(random.uniform(8.0, 17.0), 1)
                    births = int(avg_pop * birth_rate / 1000)
                    deaths = int(avg_pop * death_rate / 1000)
                    natural_growth = births - deaths
                    natural_growth_rate = round(birth_rate - death_rate, 1)
                    net_migration_rate = round(random.uniform(-15.0, 20.0), 1)
                    net_migration = int(avg_pop * net_migration_rate / 1000)

                    di = DemographicIndicator(
                        municipality_id=muni.id,
                        year=year,
                        births=births,
                        deaths=deaths,
                        natural_growth=natural_growth,
                        migration_in=max(0, net_migration + abs(net_migration) // 2),
                        migration_out=max(0, abs(net_migration) // 2 - net_migration // 3),
                        net_migration=net_migration,
                        birth_rate=birth_rate,
                        death_rate=death_rate,
                        natural_growth_rate=natural_growth_rate,
                        net_migration_rate=net_migration_rate,
                    )
                    db.add(di)

        await db.commit()
        print(f"Seeded {len(region_map)} regions and {muni_count} municipalities.")


if __name__ == "__main__":
    asyncio.run(seed_database())
